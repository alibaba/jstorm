package com.alibaba.jstorm.ui.model;

/**
 * @author Jark (wuchong.wc@alibaba-inc.com)
 */
public class ZooKeeperEntity {

    private String host;
    private String ip;
    private String port;

    public ZooKeeperEntity(String host, String ip, String port) {
        this.host = host;
        this.ip = ip;
        this.port = port;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }
}
